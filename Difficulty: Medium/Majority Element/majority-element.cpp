class Solution {
  public:
    int majorityElement(vector<int>& arr) {
        // code here
        int cnt=0,el=0;
        for(int i:arr){
            if(cnt==0)el=i;
            cnt+=(i==el)?-1:1;
        }
        cnt = count(arr.begin(),arr.end(),el);
        return (cnt>arr.size()/2)?el:-1;
    }
};


